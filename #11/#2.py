#além de usar o método bubble sort, existe só o método facil e easy
l = [6, 4, 2, 1, 9]
l.sort()
print(l)
print("__-__" * 100)
l = [6, 4, 2, 1, 9]
print(l)
print(sorted(l))
print(l)
print("__-__" * 100)
a = sorted(l)
print(l)
print(a)
#conclusão: o sort modifica direto da raiz e o sorted somente imprime ou salva em uma variável.
