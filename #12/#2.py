#tuplas também podem ser usadas para empacotamento, como:

a = 100, 200, 300 # qunado tem mais de um elemento assim, já é automaticamente considerado uma tupla
print(a)
#empacota os valores, eles ficam todos dentro de uma parênteses

#assim como também pode desempacotar, como: 
a, b = 10, 20
print(a)
print(b)
#desempacota, meio que separa os valores

#também podemos substituir os valores:

a, b = b, a
print(a)
print(b)


