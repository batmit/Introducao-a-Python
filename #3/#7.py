#fatiamento de strings

a = 'batata'

b = len(a)

print(a[0:b]) #vai mostras os números do 0 até o b

print(a[0:2]) #vai mostrar o 0 e o 1, ele não mostra o 2

print(a[0:2 + 1]) # aí sim ele vai mostrar do 0 até o 2

print(a[0:]) # vai mostrar do 0 até o final

print(a[1:]) # vai mostrar do 1 até o final

print(a[-b:]) # vai mostrar o número de b, da direita para a esquerda. Ou seja, vai mostrar tudinho

print(a[-1:])

print(a[1:-1])