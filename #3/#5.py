# %d == inteiros
# %s == strings/booleans
# %f == decimais

print('Seu valor é %s' % True)

a = 20

print("(%03d)" % a) # vai colocar um 0 na posição 3, ou seja, antes do 2

print("(%3d)" % a) # vai mostrar a posição três mas vazia

print("(%-3d)" % a) # vai mostrar a posição -3, ou seja, depois do zero, sem nada